package day02_DataTurleriveScanner;

public class C02_NonPrimitiveDataTurleri {
    public static void main(String[] args) {
        int sayi = 10;

        String str = "Java candir";
        System.out.println(sayi); //10
        System.out.println(str); // Java candir
        System.out.println(str.toLowerCase());//java candir
        System.out.println(str.toUpperCase()); //JAVA CANDIR

        /*

        1- Primitive data turleri sadece deger saklayabilir.
        Non primitive data turleri ise deger saklamanin yaninda hazir methodlarda barindirir.
        Bu hazir methodlar ssayesinde biz tanimlanimis islemleri kolayca yapabiliriz.
        2- primitive data turlerinin isimleri kucuk harfle baslar.
        Non primitive " "   "      "    "     BUYUK harfle baslar.

        String metinsel ifadeler icerir.

         */
    }
}
